def checker(string):
    if string.isalnum():
        print(True)
    else:
        print(False)
    if string.isalpha():
        print(True)
    else:
        print(False)
    if string.isdigit():
        print(True)
    else:
        print(False)
    if string.islower():
        print(True)
    else:
        print(False)
    if string.isupper():
        print(True)
    else:
        print(False)
checker("qA2")